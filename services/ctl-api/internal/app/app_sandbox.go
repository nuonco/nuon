package app

import (
	"time"

	"github.com/powertoolsdev/mono/pkg/shortid/domains"
	"gorm.io/gorm"
	"gorm.io/plugin/soft_delete"
)

type AppSandbox struct {
	ID          string                `gorm:"primarykey;check:id_checker,char_length(id)=26" json:"id"`
	CreatedByID string                `json:"created_by_id" gorm:"not null;default:null"`
	CreatedAt   time.Time             `json:"created_at"`
	UpdatedAt   time.Time             `json:"updated_at"`
	DeletedAt   soft_delete.DeletedAt `gorm:"index:idx_app_sandbox,unique" json:"-"`

	OrgID string `json:"org_id" gorm:"notnull;default null"`
	AppID string `json:"app_id" gorm:"index:idx_app_sandbox,unique"`

	AppSandboxConfigs []AppSandboxConfig `json:"app_sandbox_configs" gorm:"constraint:OnDelete:CASCADE;"`
}

func (a *AppSandbox) BeforeCreate(tx *gorm.DB) error {
	if a.ID == "" {
		a.ID = domains.NewAppID()
	}

	a.CreatedByID = createdByIDFromContext(tx.Statement.Context)
	a.OrgID = orgIDFromContext(tx.Statement.Context)
	return nil
}
